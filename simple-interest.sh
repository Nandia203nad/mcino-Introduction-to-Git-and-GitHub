#!/bin/bash
# Энэхүү скрипт нь хэрэглэгчийн оруулсан өгөгдөл дээр үндэслэн энгийн хүүг бодно.
# Ашиглах томьёо: SI = (Principal * Rate * Time) / 100

echo "--------------------------------------------------"
echo "   Энгийн хүү бодогч програм (Simple Interest)    "
echo "--------------------------------------------------"

# 1. Хэрэглэгчээс оролтын талбаруудыг асууж авах
read -p "Үндсэн хөрөнгө (Principal): " principal
read -p "Жилийн хүүгийн хувь % (Rate of Interest): " rate
read -p "Хугацаа /жилээр/ (Time Period): " time

# 2. Энгийн хүүг 'bc' ашиглан бутархай орны нарийвчлалтай бодох
# 'scale=2' нь таслалаас хойш 2 орон харуулна гэдгийг зааж байна.
simple_interest=$(echo "scale=2; ($principal * $rate * $time) / 100" | bc)

# 3. Нийт үнийн дүнг бодох (Үндсэн хөрөнгө + Хүү)
total_amount=$(echo "scale=2; $principal + $simple_interest" | bc)

# 4. Үр дүнг дэлгэцэнд тодорхой харуулах
echo "--------------------------------------------------"
echo "Бодогдсон энгийн хүү: $simple_interest"
echo "Нийт эргэн төлөгдөх дүн: $total_amount"
echo "--------------------------------------------------"
